class LSTMModel(nn.Module):
    """
    Seq2seq LSTM forecaster with the same interface as Informer.
    Uses DataEmbedding(x, x_mark) for fair comparison, encodes the
    history, then decodes over [label_len + pred_len] and returns the
    last pred_len steps.
    """
    def __init__(
        self, enc_in, dec_in, c_out, seq_len, label_len, out_len,
        factor=5, d_model=512, n_heads=8, e_layers=3, d_layers=2, d_ff=512,
        dropout=0.0, attn='prob', embed='fixed', freq='h', activation='gelu',
        output_attention=False, distil=True, mix=True,
        device=torch.device('cuda:0')
    ):
        super().__init__()
        self.pred_len = out_len
        self.output_attention = output_attention  # kept for API compatibility

        # Embeddings (same as Informer so inputs are identical)
        self.enc_embedding = DataEmbedding(enc_in, d_model, embed, freq, dropout)
        self.dec_embedding = DataEmbedding(dec_in, d_model, embed, freq, dropout)

        # Encoder/Decoder LSTMs
        self.encoder = nn.LSTM(
            input_size=d_model, hidden_size=d_model,
            num_layers=e_layers, dropout=(dropout if e_layers > 1 else 0.0),
            batch_first=True
        )
        self.decoder = nn.LSTM(
            input_size=d_model, hidden_size=d_model,
            num_layers=d_layers, dropout=(dropout if d_layers > 1 else 0.0),
            batch_first=True
        )

        # Output projection
        self.projection = nn.Linear(d_model, c_out)

    def forward(self, x_enc, x_mark_enc, x_dec, x_mark_dec,
        enc_self_mask=None, dec_self_mask=None, dec_enc_mask=None):
    # Encode
        enc_inp = self.enc_embedding(x_enc, x_mark_enc)          # [B, L_enc, d_model]
        _, (h_e, c_e) = self.encoder(enc_inp)                     # h_e: [e_layers, B, d_model]

    # Prepare decoder init state: repeat last layer to match d_layers
    # (handles e_layers != d_layers safely)
        h0 = h_e[-1:].repeat(self.decoder.num_layers, 1, 1).contiguous()
        c0 = c_e[-1:].repeat(self.decoder.num_layers, 1, 1).contiguous()

    # Decode teacher-forced context + horizon
        dec_inp = self.dec_embedding(x_dec, x_mark_dec)           # [B, L_dec, d_model]
        dec_out, _ = self.decoder(dec_inp, (h0, c0))              # [B, L_dec, d_model]

    # Project and return the horizon
        dec_out = self.projection(dec_out)                        # [B, L_dec, c_out]
        return dec_out[:, -self.pred_len:, :]                     # [B, pred_len, c_out]
